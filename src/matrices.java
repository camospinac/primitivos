public class matrices {
    public static void main(String[] args) {
        // Definicion matriz 2x3
        int [][] matriz = new int[2][2];
        //Insertar valores a la matriz
        matriz[0][0]= 200;
        matriz[0][1]= 300;
        matriz[1][0]= 400;
        matriz[1][1]= 600;
        System.out.println("Valores de la matriz [0][0]: " + matriz[0][0]);
        System.out.println("Valores de la matriz [0][1]: " + matriz[0][1]);
        System.out.println("Valores de la matriz [1][0]: " + matriz[1][0]);
        System.out.println("Valores de la matriz [1][1]: " + matriz[1][1]);
    }
    
}
